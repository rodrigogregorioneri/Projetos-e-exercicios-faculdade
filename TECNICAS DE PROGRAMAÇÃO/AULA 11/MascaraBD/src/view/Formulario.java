    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Paciente p = montaPaciente();
        String resp = new PacienteDAO().gravarPaciente(p);        
        if(resp.equals("OK")) {
            JOptionPane.showMessageDialog(null, "Paciente Gravado com sucesso");
            limparCampos();
            carregarLista();
        }
        else {
            JOptionPane.showMessageDialog(null, "Erro ao gravar.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (tabela01.getSelectedRow() <0){
            JOptionPane.showMessageDialog(null, "Selecione um registro.");
        }
        else
        {
            if (new PacienteDAO().delPaciente(Integer.parseInt(
                                                        tabela01.getModel().getValueAt(
                                                            tabela01.getSelectedRow(), 0).toString()))){            
                JOptionPane.showMessageDialog(null, "Registro excluído.");
                limparCampos();
                carregarLista();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Registro não encontrado.");
            }
        }        
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
    public Paciente montaPaciente(){
        Paciente p = new Paciente();
        p.setPaciente(nome.getText());
        p.setNascimento(nascimento.getText());
        p.setSexo(sexo.getSelectedItem().toString());        
        return p;
    }   
    
    public void limparCampos(){
        nome.setText("");
        nascimento.setText("");
        sexo.setSelectedIndex(0);        
    }
    
    
