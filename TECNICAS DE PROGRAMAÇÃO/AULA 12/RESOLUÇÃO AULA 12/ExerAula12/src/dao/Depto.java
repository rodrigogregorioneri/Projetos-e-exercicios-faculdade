package dao;

public class Depto {
   private int numDepto;
   private String nomeDepto;
   private String nomeCentro;

    /**
     * @return the numDepto
     */
    public int getNumDepto() {
        return numDepto;
    }

    /**
     * @param numDepto the numDepto to set
     */
    public void setNumDepto(int numDepto) {
        this.numDepto = numDepto;
    }

    /**
     * @return the nomeDepto
     */
    public String getNomeDepto() {
        return nomeDepto;
    }

    /**
     * @param nomeDepto the nomeDepto to set
     */
    public void setNomeDepto(String nomeDepto) {
        this.nomeDepto = nomeDepto;
    }

    /**
     * @return the nomeCentro
     */
    public String getNomeCentro() {
        return nomeCentro;
    }

    /**
     * @param nomeCentro the nomeCentro to set
     */
    public void setNomeCentro(String nomeCentro) {
        this.nomeCentro = nomeCentro;
    }
   
}
