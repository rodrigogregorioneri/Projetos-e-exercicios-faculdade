    public Aluno montaAluno(){
        Aluno aluno = new Aluno();
        aluno.setRGM(jTextField1.getText());
        aluno.setNome(jTextField2.getText());
        aluno.setCurso(jComboBox1.getSelectedItem().toString());
        aluno.setEmail(jTextField3.getText());
        return aluno;
    }

    public void exibeAluno(Aluno aluno){
        jTextField2.setText(aluno.getNome());
        jTextField3.setText(aluno.getEmail());
        jComboBox1.setSelectedItem(aluno.getCurso());
    }
    
    public void limparCampos(){
        jTextField1.setText("");
        jTextField2.setText("");
        jComboBox1.setSelectedIndex(0);
        jTextField3.setText("");
    }

