
package dao;


public class Aluno {
    
    private String rgm;
    private String nome;

    /**
     * @return the rgm
     */
    public String getRgm() {
        return rgm;
    }

    /**
     * @param rgm the rgm to set
     */
    public void setRgm(String rgm) {
        this.rgm = rgm;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
