    public void carregarLista() {
        //Captura o modelo que preenche a tabela
        DefaultTableModel modelo = (DefaultTableModel) tabela01.getModel();        
        ArrayList<Paciente> lista = new PacienteDAO().listaPacientes();
        modelo.setNumRows(0);
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new String[]{ lista.get(i).getCodigo()+"", 
                                        lista.get(i).getPaciente(),
                                        lista.get(i).getNascimento(),
                                        lista.get(i).getSexo()
                                        }
                          );
        }  
        //configura o codigo com o número do prox registro
        codigo.setText(new PacienteDAO().getLastCodigo()+"");
    }

