        private void Button_Click(object sender, RoutedEventArgs e)
        {
            float a, b, res = 0.0f;
            try
            {
                a = Convert.ToSingle(txt1.Text);
                b = Convert.ToSingle(txt2.Text);
                if (rbsomar.IsChecked == true) res = a + b;
                if (rbmultiplicar.IsChecked == true) res = a * b;
                lblres.Text = "Resultado: " + res;
            }
            catch (Exception ex)
            {
                lblres.Text = "Por favor, digite valores corretos.";
            }
        }

