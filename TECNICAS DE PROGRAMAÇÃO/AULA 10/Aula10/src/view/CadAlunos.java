    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        List<Aluno> alunos = new AlunoDAO().listarAlunos();
	int i = 0;
	jTextArea1.setText("");
        if (alunos.size()>0){
            while (i < alunos.size()) {
		Aluno a = alunos.get(i);
                jTextArea1.append("Aluno " + (i + 1) + "\n");
		jTextArea1.append("RGM: " + a.getRGM() + ", ");
		jTextArea1.append("Nome: " + a.getNome() + ", ");
		jTextArea1.append("Curso: " + a.getCurso() + ", ");
		jTextArea1.append("Email: " + a.getEmail() + "\n\n");		
		i++;
            }			
	}
	else
            JOptionPane.showMessageDialog(null,"Banco de dados vazio.");
    }//GEN-LAST:event_jButton5ActionPerformed

